/*  Arrays */ 
/*Array Methods('push','slice','indexOf','length')*/

const groceries = ['Mango', 'Apple', 'Orange', 'Pear', 'Peach']
// console.log(groceries)

// grab the index 2
console.log(groceries[1])

// Arrays Method
groceries.push('Peach')
console.log(groceries)

groceries.push('Pineapple')
console.log(groceries)

groceries.push('Coconut')
console.log(groceries)

groceries.push('Strawberry')
console.log(groceries)

// Array slice
console.log(groceries.slice(2, 5))
// Array indexOf
console.log(groceries.indexOf('Peach'))

// Array length
console.log(groceries.length)

